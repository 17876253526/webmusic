package entity;

public class Kind {
    private Integer kindId;

    private Integer kindKindIncrease;

    private Integer kindBaseKind;

    public Integer getKindId() {
        return kindId;
    }

    public void setKindId(Integer kindId) {
        this.kindId = kindId;
    }

    public Integer getKindKindIncrease() {
        return kindKindIncrease;
    }

    public void setKindKindIncrease(Integer kindKindIncrease) {
        this.kindKindIncrease = kindKindIncrease;
    }

    public Integer getKindBaseKind() {
        return kindBaseKind;
    }

    public void setKindBaseKind(Integer kindBaseKind) {
        this.kindBaseKind = kindBaseKind;
    }
}